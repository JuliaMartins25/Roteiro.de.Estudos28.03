//estruturas de repetição

//Declaro variável A para exibir sequência de números
console.log('sequencia utilizando while:')
let a = 1;

//estrutura de repetição WHILE para executar a sequência
while ( a <= 10){
    console.log('sequencia completa:' + a);
    a++
}

//Declaro outra variavel para exibir sequência de números
console.log('sequencia utilizando FOR:')
let b = 1;

//estrutura de repetição FOR para executar a sequência
for (let i = b; i <= 10; i++){
    console.log('sequencia completa:' + i);
}